function obtenerdatos()
	{
		var titulo = document.getElementById('titulo').value;
		var descripcion = document.getElementById('descripcion').value;
		var generon = parseFloat(document.getElementById('genero').value);

		document.registro.tituloobtenido.value = titulo;
		document.registro.descripcionobtenida.value = descripcion;
		document.registro.generoonobtenida.value = genero;

    }
